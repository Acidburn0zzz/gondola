package com.yahoo.gondola.demo;

/**
 * A class that holds the key/value pairs in the hash table.
 */
public class Entry {
    String key;
    String value;

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
